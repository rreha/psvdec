G_DICT_SIZE = 1024
G_DICT = bytes(G_DICT_RAW)[:G_DICT_SIZE]

if len(G_DICT) < G_DICT_SIZE:
    G_DICT += bytes([0] * (G_DICT_SIZE - len(G_DICT)))

def deflate_key(license_data: bytes) -> bytes:
    try:
        compressor = zlib.compressobj(
            level=9,
            method=zlib.DEFLATED,
            wbits=10,
            memLevel=8,
            strategy=zlib.Z_DEFAULT_STRATEGY,
            zdict=G_DICT
        )
        return compressor.compress(license_data) + compressor.flush()
    except zlib.error as e:
        raise ValueError(f"Compression failed: {e}")

def inflate_key(compressed_data: bytes) -> bytes:
    try:
        decompressor = zlib.decompressobj(
            wbits=10,
            zdict=G_DICT
        )
        return decompressor.decompress(compressed_data) + decompressor.flush()
    except zlib.error as e:
        raise ValueError(f"Decompression failed: {e}")

def get_content_id(license_data: bytes) -> str:
    if len(license_data) < 0x40:
        return ""
    try:
        return license_data[0x10:0x40].rstrip(b'\x00').decode('utf-8')
    except Exception:
        return ""